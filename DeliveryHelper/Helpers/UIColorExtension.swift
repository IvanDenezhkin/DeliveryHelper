import UIKit

extension UIColor {
    static var lowBlack: UIColor {
        return UIColor(red: 35/255, green: 22/255, blue: 12/255, alpha: 1)
    }
    
    static var highBlack: UIColor {
        return UIColor(red: 84/255, green: 86/255, blue: 99/255, alpha: 1)
    }
}
